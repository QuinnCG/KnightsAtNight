namespace Quinn
{
	public enum StatusEffectType
	{
		// Take damage every interval.
		Burning,
		// Deal less damage.
		Weakened,
		// Move slower.
		Slowed,
		// Can't move or attack.
		Shocked,
		// Like Burning, but meant to last longer.
		GhostFireBurning,
		// Take reduced damage and move faster.
		Blessing
	}
}
